function navigateTo(section) {
    // Navigate to different sections based on the image clicked
    switch (section) {
        case 'pareja':
            window.location.href = '#pareja';
            break;
        case 'embarazo':
            window.location.href = '#embarazo';
            break;
        case 'bebe':
            window.location.href = '#bebe';
            break;
    }
}
